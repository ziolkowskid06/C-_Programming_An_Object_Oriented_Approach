/***************************************************************
 * Write a program that prints figure using asterisks.         *
 ***************************************************************/
#include <iostream>

using namespace std;

int main()
{
	char symbol = '*';

	cout << symbol << endl;
	cout << symbol << symbol << endl;
	cout << symbol << symbol << symbol << endl;
	cout << symbol << symbol << endl;
	cout << symbol << endl;
	return 0;
}