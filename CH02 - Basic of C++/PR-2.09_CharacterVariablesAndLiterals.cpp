/***************************************************************
 * Using some char variables and initialize them               *
 ***************************************************************/
#include <iostream>

using namespace std;

int main ()
{
	// Defining and initializing some variables of char type
	char first = 'A';
	char second = 65;
	char third = 'B';
	char fourth = 66;

	// Printing values
	cout << "Value of first: " << first << endl;
	cout << "Value of second: " << second << endl;
	cout << "Value of third: " << third << endl;
	cout << "Value of fourth: " << fourth;
	return 0;
}

/*
	Run:
	Value of first: A
	Value of second: A
	Value of third: B
	Value of fourth: B
*/